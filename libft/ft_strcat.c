#include "libft.h"

void	ft_strcat(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i])
		i++;
	while (*s2)
	{
		s1[i] = *s2;
		s2++;
		i++;
	}
	s1[i] = '\0';
}
